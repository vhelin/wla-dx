static int _is_label_exportable(struct debug_label *label) {

  if (label->status != LABEL_STATUS_LABEL && label->status != LABEL_STATUS_SYMBOL && label->status != LABEL_STATUS_BREAKPOINT)
    return NO;
  if (label->is_anonymous == YES)
    return NO;

  return YES;
}


static int _str_eq(const char *a, const char *b) {

  while (*a != 0 && *b != 0) {
    if (tolower((unsigned char)*a) != tolower((unsigned char)*b))
      return NO;
    a++;
    b++;
  }

  if (*a == 0 && *b == 0)
    return YES;

  return NO;
}


static int _append_format_flag(const char *format, int *flags) {

  if (_str_eq(format, "vice") == YES)
    *flags |= DEBUG_EXPORT_VICE;
  else if (_str_eq(format, "rgbds") == YES || _str_eq(format, "bgb") == YES || _str_eq(format, "sameboy") == YES)
    *flags |= DEBUG_EXPORT_RGBDS;
  else if (_str_eq(format, "mesen") == YES)
    *flags |= DEBUG_EXPORT_MESEN;
  else if (_str_eq(format, "emulicious") == YES)
    *flags |= DEBUG_EXPORT_EMULICIOUS;
  else if (_str_eq(format, "cspect") == YES || _str_eq(format, "sld") == YES)
    *flags |= DEBUG_EXPORT_CSPECT;
  else if (_str_eq(format, "nocash") == YES || _str_eq(format, "no$gmb") == YES || _str_eq(format, "no$sns") == YES)
    *flags |= DEBUG_EXPORT_NOCASH;
  else if (_str_eq(format, "mame") == YES || _str_eq(format, "mamecmd") == YES || _str_eq(format, "mame-cmd") == YES)
    *flags |= DEBUG_EXPORT_MAME;
  else if (_str_eq(format, "json") == YES)
    *flags |= DEBUG_EXPORT_JSON;
  else {
    print_text(NO, "DEBUG_EXPORT: Unknown debug export format \"%s\".\n", format);
    return FAILED;
  }

  return SUCCEEDED;
}


int debug_export_parse_flags(const char *formats, int *flags) {

  char tmp[128];
  int i, j;

  if (formats == NULL || flags == NULL)
    return FAILED;

  i = 0;
  while (formats[i] != 0) {
    while (formats[i] == ',' || isspace((unsigned char)formats[i]) != 0)
      i++;
    if (formats[i] == 0)
      break;
    j = 0;
    while (formats[i] != 0 && formats[i] != ',' && isspace((unsigned char)formats[i]) == 0) {
      if (j >= (int)sizeof(tmp)-1) {
        print_text(NO, "DEBUG_EXPORT: Format name is too long.\n");
        return FAILED;
      }
      tmp[j++] = formats[i++];
    }
    tmp[j] = 0;
    if (_append_format_flag(tmp, flags) == FAILED)
      return FAILED;
  }

  return SUCCEEDED;
}


static int _make_output_name(char *dst, int dst_size, char *outname, const char *extension) {

  int len, i;

  if (dst == NULL || outname == NULL || extension == NULL)
    return FAILED;

  len = (int)strlen(outname);
  if (len + (int)strlen(extension) + 1 >= dst_size) {
    print_text(NO, "DEBUG_EXPORT: File name too long.\n");
    return FAILED;
  }

  strcpy(dst, outname);
  i = len - 1;
  while (i >= 0 && dst[i] != '.' && dst[i] != '/' && dst[i] != '\\')
    i--;
  if (i < 0 || dst[i] == '/' || dst[i] == '\\')
    i = len;
  strcpy(&dst[i], extension);

  return SUCCEEDED;
}


static FILE *_open_output(char *outname, const char *extension) {

  char name[MAX_NAME_LENGTH + 1];
  FILE *f;

  if (_make_output_name(name, sizeof(name), outname, extension) == FAILED)
    return NULL;

  f = fopen(name, "wb");
  if (f == NULL)
    print_text(NO, "DEBUG_EXPORT: Error opening file \"%s\" for writing.\n", name);

  return f;
}


static int _write_vice(struct debug_model *model, char *outname) {

  FILE *f;
  int i;

  f = _open_output(outname, ".vic");
  if (f == NULL)
    return FAILED;

  fprintf(f, "; VICE labels generated by WLALINK\n");
  for (i = 0; i < model->label_count; i++) {
    struct debug_label *label;

    label = &model->labels[i];
    if (_is_label_exportable(label) == YES)
      fprintf(f, "al %04x .%s\n", label->address & 0xffff, label->name);
  }

  fclose(f);
  return SUCCEEDED;
}


static int _write_rgbds_like(struct debug_model *model, char *outname, const char *extension, const char *title) {

  FILE *f;
  int i;

  f = _open_output(outname, extension);
  if (f == NULL)
    return FAILED;

  fprintf(f, "; %s symbols generated by WLALINK\n", title);
  for (i = 0; i < model->label_count; i++) {
    struct debug_label *label;

    label = &model->labels[i];
    if (_is_label_exportable(label) == YES)
      fprintf(f, "%02x:%04x %s\n", (label->bank + label->base) & 0xff, label->address & 0xffff, label->name);
  }

  fclose(f);
  return SUCCEEDED;
}


